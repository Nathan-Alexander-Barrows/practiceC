#include <stdio.h>

/**
 * Include standard input-output library
 */
int main(void)
{
	char grade = 'A';
	/**
	 * declaring a charater variable named grade and initializing it
	 * with the character 'A'
	 */
	char letter = 'B';
	/**
	 * declaring a variable named letter and initializing it
	 * with the character 'B'
	 */
	char digit = '3';
	/**
	 * declaring a char variable named digit and initializing it
	 * with the character '3'
	 */
	char symbol = '#';
	/**
	 * declaring a char variable named symbol and initializing it
	 * with the character '#'
	 */

	/**
	 * print the values of the char variables
	 */
	printf("grade: %c\n", grade);
	/**
	 * modulus C (%c) format specifier is used to print a char (character type)
	 */
	printf("letter: %c\n", letter);
	/**
	 * prints the value of "letter"
	 */
	printf("digit: %c\n", digit);
	/**
	 * prints the value of "digit"
	 */
	printf("symbol: %c\n", symbol);
	/**
	 * prints the value of "symbol"
	 */
/**
 * return (0); to indicate that the program ended succesfully
 */
	return (0);
}
